;主引导程序
;---------------------------------------
SECTION MBR vstart=0x7c00
    mov ax,cs
    mov ds,ax
    mov es,ax
    mov ss,ax
    mov fs,ax
    mov sp,0x7c00

;清屏利用0x06号功能，上卷全部行，则可清屏
;---------------------------------------
;INT 0x10    功能号    功能描述：上卷窗口
;---------------------------------------
;输入：
;AH 功能号 = 0x06
;AL = 上卷的行数（0表示全部上卷）
;BH = 上卷后窗口底端的属性
;(CL,CH) = 上卷后窗口左上角的坐标(X,Y)
;(DL,DH) = 上卷后窗口右下角的坐标(X,Y)
;无返回值
    mov ax,0x0600
    mov bx,0x0700
    mov cx,0x0000   ;左上角:(0,0)
    mov dx,0x184f   ;右下角:(80,25)
                    ;VGA文本模式下，每行80个字符，每个字符占2个字节，共25行
                    ;下标从0开始，所以0x18=24，0x4f=79
    int 0x10        ;int 0x10

;获取光标位置结束

;打印字符串
;---------------------------------------
;使用10h中断，调用13号子功能打印字符串
;---------------------------------------
    mov ax,message
    mov bp,ax       ;es:bp 为串首地址，es此时同cs一致
                    ;开头时已经为sreg初始化

    ;光标位置要用到dx寄存器中内容，cx中的光标位置可忽略
    mov cx,5
    mov ax,0x1301

    mov bx,0x2
    int 0x10        ;int 0x10

;打印字符串结束
    jmp $
    message db 'hello,world! 1 MBR!!!'
    times 510-($-$$) db 0
    dw 0x55,0xaa